const TWITTER_WIDGETS_SRC = 'https://platform.twitter.com/widgets.js'

const loadTwitterWidgets = (onReady) => {
    if (window.twttr?.widgets) {
        onReady()
        return
    }
    let script = document.querySelector(`script[src="${TWITTER_WIDGETS_SRC}"]`)
    if (script) {
        if (window.twttr?.widgets) onReady()
        else script.addEventListener('load', onReady, { once: true })
        return
    }
    script = document.createElement('script')
    script.src = TWITTER_WIDGETS_SRC
    script.async = true
    script.charset = 'utf-8'
    script.addEventListener('load', onReady, { once: true })
    document.head.appendChild(script)
}

const loadTweets = () => {
    window.twttr?.widgets?.load()
}

const prallaxGallery = () => {
    let gallery  = document.querySelector('.gallery');
    let columns  = document.querySelectorAll('.gallery .column');

    if(document.body.getBoundingClientRect().width >= 1024){
    
    columns.forEach((col) => {
        let tline = gsap.timeline({
            scrollTrigger: {
                trigger: gallery,
                start: 'top bottom',
                end: 'bottom top',
                scrub: true,
            }
        })

        tline.fromTo(columns[0], { yPercent: 0}, {yPercent: 65}, "<");
        tline.fromTo(columns[2], { yPercent: 0}, {yPercent: 60}, "<");
        tline.fromTo(columns[1], { yPercent: 0}, {yPercent: 115}, "<");
        tline.fromTo(columns[3], { yPercent: 0}, {yPercent: 105}, "<");

    })
    }
}

onMounted(() => {
    loadTwitterWidgets(() => {
        loadTweets()
        requestAnimationFrame(() => prallaxGallery())
    })
})
